package ex5.logic;

/**
 * The enum Variable type.
 */
public enum VariableType {
    /**
     * Int variable type.
     */
    INT,
    /**
     * Double variable type.
     */
    DOUBLE,
    /**
     * String variable type.
     */
    STRING,
    /**
     * Boolean variable type.
     */
    BOOLEAN,
    /**
     * Char variable type.
     */
    CHAR;


}
